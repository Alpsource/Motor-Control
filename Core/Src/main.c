float PID_update(PID_t *pid, float error)
{
	assert(pid);

	pid->ediff = error - pid->error;
	pid->esum += error;
	pid->error = error;

	return (pid->control = (pid->Kp * pid->error) + (pid->Ki * pid->esum) + (pid->Kd * pid->ediff));
}

